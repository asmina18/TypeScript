// Array med strings
let stringArray: string[] = ["abc", "def", "ghi"];

// Array med tal
let numberArray: number[] = [1, 2, 3, 4, 5];

// Mixed array
let mixedArray: (string | number | boolean)[] = ["abc", 123, true, "def", 456];


console.log(stringArray); // ["abc", "def", "ghi"]
console.log(numberArray); // [1, 2, 3, 4, 5]
console.log(mixedArray);  // ["abc", 123, true, "def", 456]
